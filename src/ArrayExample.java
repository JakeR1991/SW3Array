public class ArrayExample {
    public static void main(String[] args) {
        // use array instead data_type [] varName = { values}
        String[] car = {"Ford", "BMW", "Ford", "BMW", "Ford", "BMW"}; // six values, index = 0-5
        // call the element of the array, arrayName [index]
        System.out.println(car[0]);
    }
}